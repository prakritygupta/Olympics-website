            </div>  
          <p class="athlete-info"><h4> Rowing is the propelling of a boat using a fixed oar as a lever. In modern sports, rowers race against each other as individuals or in crews of two, four or eight. </h4>
          </div>  
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>The Boat Race</h1></p>
            </div>  
          <p class="athlete-info"><h4>Rowing was first used as a means of transport in ancient Egypt, Greece and Rome. As a sport, it probably began in England in the 17th and early 18th centuries, with the Oxford-Cambridge university boat race, which was inaugurated in 1828. By the 19th century, rowing was popular in Europe and had been exported to America.
                    </h4>
             <br>        
          </div>    
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Sculling vs. Sweep</h1></p>
              <br>
            </div>  <h4>The races are divided into sculling and sweep oar. Sculling events use two oars, whilst in sweep, the rower holds one. The eight-person crews have a coxswain, who steers the boat and directs the crew, but in all other boats, one rower steers by controlling a small rudder with a foot pedal.
                     </h4>
          <br>        
          </div>
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Athlete of the Century</h1></p>
            </div> <h4>Sir Steve Redgrave of Great Britain is widely hailed as the greatest rower ever. A six-time World Champion, he won gold medals at five Olympic Games and has been loosely crowned Athlete of the Century because of the extreme physical demands of rowing. His female counterpart on the gold medal count is Elisabeta Lipa of Romania, who also won five Olympic gold medals between 1984 and 2004.
                    </h4>
               
          </div>
