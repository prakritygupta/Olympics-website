            </div>  
          <p class="athlete-info"><h4> Bicycles were first developed in the mid-18th century and have been a popular form of transport ever since.In 1885, cycling took a big leap forward when J.K. Starley devised the chain-and-gearing system. Since then, engineers have embraced modern technology to build ever faster, sleeker and lighter bikes.
                   
                    From 1880 to 1900, cycling became immensely popular both in Europe and the United States. The professional sport maintained its place in the public imagination, especially in Europe. Over the last 20 years, road cycling has experienced a movement towards globalisation.
                   
                         
        </h4>
