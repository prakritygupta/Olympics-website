              <p><h1>HISTORY</h1> </p>
            </div>  
          <p class="athlete-info"><h4>Throughout recorded sports history, athletics has always been practised. The first event contested in the ancient Olympic Games was the stadium race, a sprint of about 192 metres. Winners in this event have been recorded from as far back as 776 BC.
            </h4>
          </div>  
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Ancient Events</h1></p>
            </div>  
          <p class="athlete-info"><h4>Much like today, the ancient Olympic Games included a wide variety of track and field events. Such events included longer foot races, a race in armour, and a pentathlon event that consisted of the stadium race, long jump, discus throw, javelin throw and wrestling.
           </h4>
             <br>        
          </div>    
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Across the Globe</h1></p>
            </div>  
          <p class="athlete-info"><h4>Similar events were held in ancient Greece at the Isthmian, Nemean and Pythian Games. Throughout Europe, local fairs and festivals often included running, jumping and throwing events. In Ireland and Scotland, these developed into organised sets of Tailteann and Highland Games, respectively. There was also a tradition of pedestrian events  often with heavy betting involved  in the 18th and 19 centuries, especially in Great Britain, which normally involved walking or running races.
            </h4>
          <br>        
          </div>
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Modern Organisation</h1></p>
            </div>  
          <p class="athlete-info"> <h4>The modern format of athletics, in which a variety of running, jumping, throwing, walking and combined events are competed at a single meeting or meet, evolved in the late 19th century, when schools and military colleges began to incorporate sports and exercise as part of education programmes. The earliest recorded meeting dates back to 1840 in Shropshire, England, but specialised championships began to thrive in the 1880s in the USA, UK, and throughout Europe, as well as in other developed nations.
          </h4>
          <br>    
          <br>
          <br>    
          </div>
