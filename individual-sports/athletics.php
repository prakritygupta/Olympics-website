            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Olympic History</h1></p>
            </div>  
          <p class="athlete-info"><h4>Since 1896, athletics has been on the programme of each edition of the Games of the Olympiad. Its presence on the Games programme has allowed its popularity to increase across the world. This popularity was also strengthened by the creation of the IAAF in 1912. Womens events appeared for the first time at the 1928 Olympic Games in Amsterdam, while the mens programme was standardised as of the 1932 Games in Los Angeles. Although, at the beginning, women were authorised to participate in only some events, today their programme is almost identical to that of the men.
                    The 1960s saw a boom in athletics in developing countries, with the success of African runners and sprinters of Caribbean origin. Americans have, for a long time, reigned over mens athletics, although European nations have also dominated. For the women, the Soviet Union and the GDR (East Germany) dominated the competitions before their dissolution.
          </h4>
