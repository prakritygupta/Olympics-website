            </h4>
          </div>  
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Invension of Table Tennis</h1></p>
            </div>  
          <p class="athlete-info"><h4> It is thought that upper-class Victorians in England invented table tennis in the 1880s as a genteel, after-dinner alternative to lawn tennis, using whatever they could find as equipment. A line of books would often be the net, the rounded top of a champagne cork would be the ball and occasionally a cigar box lid would be a racket.
                   
                    In 1926, meetings were held in Berlin and London that led to the formation of the International Table Tennis Federation.
                   
                    It is estimated there are 40 million competitive table tennis players and countless millions playing recreationally, making it the sport with the most participants worldwide.
                   
        </h4>
             <br>        
          </div>      
           
                </div>  
