              <p><h1>Rules of table tennis</h1></p>
            </div>  
          <p class="athlete-info"><h4> Exact scoring rules may differ from tournament to tournament and even round to round within an event, but generally matches are best-of-5- or best-of-7-games. Within each game, the first player to 11 points with a 2-point cushion takes the game. There are also rules regarding what type of paddles are legal.
                   
        </h4>
             <br>        
          </div>      
           
                </div>  
         
