            <div class="heading" style="margin-bottom:auto;">
              <p><h1>What is figure skating?</h1> </p>
            </div>  
          <p class="athlete-info"><h4> Figure skating, as its name suggests, involves skating "figures" on ice. The sport requires competitors to skate selected patterns, or figures, as part of their routines. Ice skating has developed from a practical way to get around on ice into the elegant mix of art and sport it is today.
                   
            </h4>
          </div>  
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>who was it invented by?</h1></p>
            </div>  
          <p class="athlete-info"><h4>   The Dutch were arguably the earliest pioneers of skating. They began using canals to maintain communication by skating from village to village as far back as the 13th century. Skating eventually spread across the channel to England, and soon the first clubs and artificial rinks began to form. Passionate skaters included several kings of England, Marie Antoinette, Napoleon III and German writer Johann Wolfgang von Goethe.
                   
                    Two Americans are responsible for the major developments in the history of the sport. In 1850, Edward Bushnell of Philadelphia revolutionised skating when he introduced steel-bladed skates allowing complex manoeuvres and turns. Jackson Haines, a ballet master living in Vienna in the 1860s, added elements of ballet and dance to give the sport its grace.
                   
                   
        </h4>
             <br>        
          </div>    
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>rules of figure skating</h1></p>
            </div>  
          <p class="athlete-info"><h4> Skaters must perform a set number of jumps in each routine in singles and pairs events and follow a set theme in ice dancing. Falls and other penalties are penalised with points deductions. The total score counts for the overall result.
                    </h4>
          <br>        
          </div>
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>How is figure skating scored?</h1></p>
            </div>  
          <p class="athlete-info"> <h4> There are two key parts to a figure skating score under the current ISU Judging System: technical elements (TES) and program components (PCS). In simple terms, TES measures the routine's technicality (such as jump difficulty and how well-executed each element—jumps, spins, et cetera—is performed) and PCS measures the routine's performance (such as music and choreography). The two parts combined determines the score for the segment (short program, rhythm dance, free skating, or free dance). A skater's final overall score is made up of the two different segment scores.
                    </h4>
          <br>    
          <br>
          <br>    
          </div>
          <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>different jumps in figure skating</h1></p>
            </div>  
          <p class="athlete-info"><h4> Single, double, triple, and quadruple jumps have been performed in figure skating. The jumps are: Axel, Euler (only single jumps, also known as a half loop), flip, loop (also sometimes called a Rittberger), Lutz, Salchow, and toe loop.
                       
        </h4>
                </div>  
                <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Figure skating and the Olympics</h1></p>
            </div>  
          <p class="athlete-info"><h4> Figure skating is the oldest sport on the Olympic Winter Games programme. It was contested at the 1908 London Games (in summer) and again in 1920 in Antwerp (also in summer), pre-dating the first Olympic Winter Games. Men’s, women’s and pairs were the three events contested until 1972. Since 1976, ice dancing has been the fourth event in the programme, proving a great success.
                   
                    Sonja Henie made her Olympic debut in Chamonix in 1924, aged just 11, and was so nervous she had to ask her coach what to do midway through her routines. However, she won gold in the next three Olympic Games and developed a huge legion of fans. She later moved into films, where she greatly increased the popularity of her sport.
                   
                    Dick Button and Hanyu Yuzuru are the only men to have won back-to-back Olympic gold medals in the sport.
                   
                     </h4>
                </div>  
               
                <div class="info">
            <div class="heading" style="margin-bottom:auto;">
              <p><h1>Best figure skaters</h1></p>
            </div>  
          <p class="athlete-info"><h4> Ilia Malinin is the only skater, male or female, to have successfully landed the most difficult jump—the quadruple Axel—in competition, and is always a hot ticket at any event. Japanese stars such as Uno Shoma, Kagiyama Yuma, Sakamoto Kaori, and Mihara Mai are also popular in singles, while Italian skaters have also begun to make their mark in men's singles, pairs, and ice dance.
                   
                                   
        </h4>
