            </div>  
          <p class="athlete-info"><h4>  Each of the four individual events—men's and women's singles, pairs, and ice dancing—will see skaters compete in the short program or rhythm dance, after which only the top few athletes (usually 24 in singles and fewer in pairs and ice dancing) progress to the free skating/dance.
                   
                    There will also be a team event, where points are awarded to each team based on their skaters' finishing position in each segment.
                   
                    </h4>
