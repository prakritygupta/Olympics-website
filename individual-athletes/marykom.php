          <p class="athlete-info">
            Mary Kom is, without doubt, one of the most fascinating athletes on the planet. It’s no wonder that she has already had a feature film made about her life: Kom’s story is remarkable. Born in a poor region of India, it looked likely that she would end up working the land with her family.<br><br>

            But – overcoming the objections from her family – she moved to the city, became a boxer after persuading a local coach to take her on, and worked her way up to becoming a champion.<br><br>

            Kom has since won a medal at all eight AIBA World Boxing Championships since its inception in 2001 (six golds, a silver and a bronze, most recently in 2019), risen to no.1 in the AIBA world rankings for a flyweight, and competed at the London 2012 Olympic Games, winning a bronze medal after being beaten by eventual champion Nicola Adams.<br><br>

            She was also the first Indian woman to win a gold medal at the Asian Games (in 2014) and at the Commonwealth Games (2018).<br><br>

           But she is a trailblazer in more ways than one. Kom has combined fighting with being a mother of three young children, she runs a sports club in her native Manipur, training youngsters to box for free, and she has become a member of the Indian Upper House of Parliament, the Rajya Sabha, campaigning on numerous issues for her fellow citizens.

