            Saina Nehwal is the first-ever badminton player from India to clinch an Olympic medal. The Indian shuttler created history when she won the bronze medal at the London 2012 Games.<br><br>

            The Haryana shuttler started turning heads very early on in her career when she won the BWF World Junior Championships in 2008. The same year she made her first Olympics appearance in Beijing, but it was only at London 2012 that she gained worldwide fame.<br><br>

