          <p class="athlete-info">
            Lí Chong-úi; born 21 October 1982) 
            is a Malaysian former badminton player. 
            As a singles player, Lee was ranked first worldwide for 349 weeks,
             including a 199-week streak from 21 August 2008 to 14 June 2012.<br><br>
             He is the fifth Malaysian player after Foo Kok Keong, Rashid Sidek, 
             Roslin Hashim and Wong Choong Hann to achieve such a ranking 
             (since official rankings were first kept in the 1980s), and is
              the only Malaysian shuttler who has held the number one ranking for more than a year.<br><br>
            Lee is a triple silver medalist at the Olympic Games, and the sixth Malaysian to
             win an Olympic medal.
             He won his first silver medal in 2008, also the first time a
              Malaysian had reached the finals in the men's singles event.
               This achievement earned him the title Datuk, and led to then 
               Malaysian Prime Minister Najib Razak describing him as national hero.
               He repeated the achievement twice more in 2012 and 2016, thus making him the most 
               successful Malaysian Olympian in history.
