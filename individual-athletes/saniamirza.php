          <p class="athlete-info">
            It's not hard to see why Sania Mirza is the finest women's tennis player India has produced. In January 2020, when she returned to the court after her maternity break, she had neither lost any of her zest for the sport nor, and more importantly for her game, that formidable power in her forehand.<br><br>

            It was the same Sania Mirza who had been a six-time Grand Slam champion, a former world No. 1 in doubles and a four-time Olympian.<br><br>

            With 42 WTA doubles titles to her name, the Rio 2016 Olympics semi-finalist is currently the most successful active doubles participant. And even though she’s the only Indian to have broken into the top 30 of the WTA singles rankings, Sania Mirza has enjoyed the bulk of her success in doubles.<br><br>

            Her partnership with Martina Hingis, in particular, was very fruitful. A pumped-up Sania and Hingis high-fiving each other was a sight not only for Swiss and Indian fans but for tennis fans across the world. The duo, in new phases of their respective careers, fed off each others’ energies and enjoyed their tennis without inhibitions.
