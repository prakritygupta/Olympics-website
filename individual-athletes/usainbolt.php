          <p class="athlete-info">
            Usain Bolt has been, quite simply, the face of the last three Olympic Games. To win the event’s blue riband event, the 100m, just once, guarantees Olympic immortality. To do it three times at consecutive Games, and add the 200m and 4x100m relay titles to the mix, gives him a good case to be considered the greatest athlete of all time. Still only 33, he is a living legend. Add real charm and humour to the mix, and Bolt has been the superstar that the Games needed over the last decade and a bit.<br><br>

            Bolt was born on 21 August 1986 in Sherwood Content, Jamaica. He grew up as a sports-obsessed kid, excelling in football and cricket. At high school, his cricket coach encouraged him to try athletics seriously. It was a good decision, By the age of 16, in 2002, he was world junior champion.<br><br>

            By 2007, he was the fastest man in history, setting his first 100m world record, and a year later, at the Beijing 2008 Olympic Games, he rose to international superstardom. In China, Bolt triumphed in the men’s 100m sprint, and then added the 200m and 4x100m title to secure a legendary triple. He broke the world record in all three events.

