          <p class="athlete-info">Despite his young age, Indian javelin thrower Neeraj Chopra has already etched his name in the history books by becoming the first and only track and field athlete from the country to win an Olympic medal that too a gold.<br><br>
            Neeraj Chopra’s gold medal at Tokyo 2020 was also India’s second individual Olympic gold medal after shooter Abhinav Bindra’s 10m air rifle glory at Beijing 2008.<br><br>
            Heading into the Tokyo Olympics, Neeraj Chopra was, at best, a dark horse in the men’s javelin competition which featured a strong field consisting of overwhelming favourite Johannes Vetter, reigning world champion Anderson Peters, London 2012 gold medallist Keshorn Walcott, among others.<br><br>
            Come the main event, though, the Indian stepped up in a major way. Neeraj Chopra topped the qualifying round with an 86.65m throw, finishing above Vetter’s 85.64m.<br><br>
            The challenge from Vetter to Neeraj Chopra in the final never materialised; The German struggled and failed to make it into the final eight, as Chopra led the field from start to finish.</p>  
           
